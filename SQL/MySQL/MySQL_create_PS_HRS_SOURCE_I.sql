DROP TABLE IF EXISTS PS_HRS_SOURCE_I;
CREATE TABLE PS_HRS_SOURCE_I (
    HRS_SOURCE_ID     DECIMAL(15,0) PRIMARY KEY,
    EFFDT             DATE NOT NULL,
