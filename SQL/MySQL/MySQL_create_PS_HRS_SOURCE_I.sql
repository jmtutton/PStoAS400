    HRS_SOURCE_STATUS DATE,
    HRS_SOURCE_NAME   VARCHAR(30) NOT NULL,
    HRS_SOURCE_DESCR  VARCHAR(254) NOT NULL,
    HRS_SOURCE_TYPE   VARCHAR(3) NOT NULL
);
